package javaprogram;

public class StringLength {

	

	    public static void main(String Args[]) {
	        String s="India";
	        int len=0;
	        try{
	        while(true){
	               //traversing and counting
	               s.charAt( len );
	               len++; 
	                }
	        
	        }
	        
	        catch(Exception e){
		           //Out of bounds
		   }
	
	    
	        System.out.println("length of the string:::::"+len);  
	   
	    
	    
	    }
	    
	    
	    
}

